SELECT
    USER_ID,
    NICKNAME,
    CONCAT_WS(' ', CITY, STREET_ADDRESS1, STREET_ADDRESS2) AS 전체주소,
    CONCAT_WS('-', LEFT(TLNO, 3), SUBSTRING(TLNO, 4, 4), RIGHT(TLNO, 4)) AS 전화번호
FROM
    USED_GOODS_BOARD B
JOIN
    USED_GOODS_USER U
ON
    B.WRITER_ID = U.USER_ID
GROUP BY
    1, 2, 3, 4
HAVING
    COUNT(1) >= 3
ORDER BY 1 DESC