SELECT 
    A.APNT_NO, 
    P.PT_NAME, 
    P.PT_NO, 
    MCDP_CD,
    DR_NAME,
    APNT_YMD
FROM
(
    SELECT PT_NO, PT_NAME
    FROM PATIENT
) P
JOIN
(
    SELECT DR_ID, DR_NAME
    FROM DOCTOR
) D
JOIN
    APPOINTMENT AS A
ON
    P.PT_NO = A.PT_NO AND
    D.DR_ID = A.MDDR_ID
WHERE
    APNT_YMD LIKE '2022-04-13%' AND
    APNT_CNCL_YN = 'N' AND
    MCDP_CD = 'CS'
ORDER BY
    APNT_YMD